"use strict";
function add(a, b) {
    var c = a + b;
    return c;
}
function sub(a, b) {
    var c = a - b;
    return c;
}
function mul(a, b) {
    var c = a * b;
    return c;
}
function div(a, b) {
    var c = a / b;
    return c;
}
console.log(add(1, 2));
console.log(sub(3, 4));
console.log(mul(5, 6));
console.log(div(1, 2));
