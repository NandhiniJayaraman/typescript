function add(a:number,b:number):number{
    var c=a+b
    return c
}
function sub(a:number,b:number):number{
    var c=a-b
    return c
}
function mul(a:number,b:number):number{
    var c=a*b
    return c
}
function div(a:number,b:number):number{
    var c=a/b
    return c
}
console.log(add(1,2))
console.log(sub(3,4))
console.log(mul(5,6))
